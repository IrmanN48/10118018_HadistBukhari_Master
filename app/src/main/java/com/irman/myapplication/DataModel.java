package com.irman.myapplication;

public class DataModel {
    private String no, hadist, terjemahan;

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }

    public String getHadist() {
        return hadist;
    }

    public void setHadist(String hadist) {
        this.hadist = hadist;
    }

    public String getTerjemahan() {
        return terjemahan;
    }

    public void setTerjemahan(String terjemahan) {
        this.terjemahan = terjemahan;
    }
}
